# Question:05 : Weather Activity Suggestion
# Statement: Suggest an activity based on the weather (Eg. Sunny - Go for a walk, Rainy - Read a book, Snowy - Build a snowman)

Weather = str(input("Enter the Weather name: "))

if(Weather == "Sunny"):
    print("Go for a walk")
elif(Weather == "Rainy"):
    print("Read a book")
elif(Weather == "Snowy"):
    print("Build a snowman")
