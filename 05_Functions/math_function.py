# Math module : more complex mathematical operations are available in the math module.
import math

print(math.pi)  # Print the value of pi
print(math.e)  # Print the value of e
print(math.sqrt(16))  # Print the square root of 16
print(math.factorial(5))  # Print the factorial of 5
print(math.factorial(5))  # Print the factorial of 5
print(math.exp(2))  # Print e raised to the power of 2
print(math.log(100, 10))  # Print the logarithm of 100 to base 10
print(math.sin(math.pi / 2))  # Print the sine of pi/2
print(math.cos(math.pi))  # Print the cosine of pi
print(math.tan(math.pi / 4))  # Print the tangent of pi/4
print(math.degrees(math.pi))  # Convert radians to degrees
print(math.radians(180))  # Convert degrees to radians